if __name__ == "__main__":
    # Load input data
    with open('untranslated-b1.json', 'r') as f:
        data = json.load(f)

    # Create Italian-to-English mapping
    result = {}
    matched = 0

    for entry in data:
        german = entry['german']
        italian = entry['italian']

        if italian:
            if german in GERMAN_ENGLISH:
                result[italian] = GERMAN_ENGLISH[german]
                matched += 1
            else:
                result[italian] = None

    # Save results
    with open('translations-b1.json', 'w', encoding='utf-8') as f:
        json.dump(result, f, ensure_ascii=False, indent=2)

    print(f"Created translations-b1.json")
    print(f"Translated: {matched}/{len(result)} entries")
    print(f"Percentage: {matched/len(result)*100:.1f}%")
